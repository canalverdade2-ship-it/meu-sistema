RETURNS boolean LANGUAGE sql IMMUTABLE AS $$ SELECT true; $$;

CREATE OR REPLACE FUNCTION public.gsa_is_valid_cnpj(text)
RETURNS boolean LANGUAGE sql IMMUTABLE AS $$ SELECT true; $$;

CREATE OR REPLACE FUNCTION public.gsa_generate_code(text)
RETURNS text LANGUAGE sql AS $$ SELECT $1 || '-TESTE'; $$;

CREATE OR REPLACE FUNCTION public.gsa_apply_points_internal(uuid, integer, text, text, uuid, boolean)
RETURNS void LANGUAGE plpgsql AS $$ BEGIN RETURN; END; $$;


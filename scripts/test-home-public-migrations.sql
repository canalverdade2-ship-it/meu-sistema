\set ON_ERROR_STOP on

CREATE SCHEMA IF NOT EXISTS extensions;
CREATE EXTENSION IF NOT EXISTS pgcrypto WITH SCHEMA extensions;

CREATE TABLE public.system_settings (
  key text PRIMARY KEY,
  value text
);

CREATE TABLE public.indicacoes (
  id uuid PRIMARY KEY DEFAULT extensions.gen_random_uuid(),
  whatsapp_indicado text,
  status text,
  data_criacao timestamptz DEFAULT now(),
  data_cadastro_indicado timestamptz,
  data_indicacao date DEFAULT current_date,
  indicador_id uuid,
  indicado_nome text,
  voucher_id uuid
);

CREATE TABLE public.clientes (
  id uuid PRIMARY KEY DEFAULT extensions.gen_random_uuid(),
  codigo_cliente text,
  nome text,
  email text,
  cpf text UNIQUE,
  cnpj text UNIQUE,
  tipo_pessoa text,
  telefone text UNIQUE,
  cep text,
  endereco text,
  numero text,
  bairro text,
  cidade text,
  estado text,
  observacoes text,
  status text,
  carteira_bloqueada boolean,
  pontos_bloqueados boolean,
  cadastro_aprovado boolean,
  bonus_boas_vindas_pendente boolean,
  indicacao_origem_id uuid,
  data_cadastro timestamptz
);

CREATE TABLE public.vouchers (
  id uuid PRIMARY KEY DEFAULT extensions.gen_random_uuid(),
  cliente_id uuid
);

CREATE TABLE public.prestadores (
  id uuid PRIMARY KEY DEFAULT extensions.gen_random_uuid(),
  tipo_cadastro text,
  nome_razao text,
  nome_responsavel text,
  documento text UNIQUE,
  email text,
  telefone text,
  cep text,
  numero text,
  area_servico text,
  observacoes text,
  status text
);

CREATE TABLE public.notificacoes (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  cliente_id uuid,
  titulo text,
  mensagem text,
  modulo text,
  tab text,
  item_id text,
  tipo text,
  destinatario_tipo text,
  prioridade text,
  acao_origem text,
  contexto jsonb
);

CREATE TABLE public.orcamentos (
  id uuid PRIMARY KEY DEFAULT extensions.gen_random_uuid(),
  cliente_id uuid,
  codigo_orcamento text,
  status text,
  categoria text,
  data_criacao date,
  titulo_solicitacao text,
  descricao_solicitacao text,
  nivel_prioridade text,
  observacoes_servico text,
  total numeric,
  valor_servico numeric,
  quantidade integer,
  origem_gsa_store boolean,
  contato_publico jsonb,
  public_request_hash text UNIQUE
);

CREATE OR REPLACE FUNCTION public.gsa_assert_public_rate_limit(text, text, integer, interval)
RETURNS void LANGUAGE plpgsql AS $$ BEGIN RETURN; END; $$;

